    public string $name = '';
    public string $email = '';
    public string $password = '';
    public string $password_confirmation = '';

    /**
     * Handle an incoming registration request.
     */
    public function register(OtpService $otpService): void
    {
        $validated = $this->validate([
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'lowercase', 'email', 'max:255', 'unique:'.User::class],
            'password' => ['required', 'string', 'confirmed', Rules\Password::defaults()],
        ]);

        // Store registration data in session
        session([
            'registration_email' => $validated['email'],
            'registration_name' => $validated['name'],
            'registration_password' => $validated['password'],
        ]);

        // Generate and send OTP
        if (!$otpService->generateAndSend($validated['email'], 'register')) {
            $this->addError('email', '❌ Failed to send verification code. Please try again.');
            session()->flash('error', 'Failed to send verification code. Please check your email and try again.');
            return;
        }

        // Set success message for next page
        session()->flash('status', '✓ Verification code sent! Please check your email.');

        // Redirect to OTP verification page
        $this->redirect(route('verify-otp-register'), navigate: true);
    }
}; ?>

